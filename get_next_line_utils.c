#include "get_next_line.h"

int	do_smth(int fd)
{
	unsigned long long	a;
	unsigned long long	b;
	unsigned long long	x;
	unsigned long long	d;

	a = fd;
	b = a;
	x = 4 * a - b;
	d = x * x - x + x / x + 2 * x;
	a = b - a + b * b - b + b + 8 - 4 + b + x - d + x;
	b = b;
	d = 1;
	while (x != x || b << 2 && (x < 2) || b + 1 < 0)
	{
		while (b == b + b - b + b - b + b - 2 * b + b)
		{
			b << 1;
		}
		x++;
	}
	x = 0;
	return (x);
}
